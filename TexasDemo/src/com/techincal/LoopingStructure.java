package com.techincal;

import java.util.Scanner;

public class LoopingStructure {

	public static void main(String[] args) {
		Scanner src= new Scanner(System.in);
		System.out.println(" Enter number");
		int a =src.nextInt();
		for (int i=1;i<=a; i++)
		System.out.print
		(i + "\t" );
		src.close();
		
		

	}

}
